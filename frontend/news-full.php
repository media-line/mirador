								</p>
								<p class="title">Различные виды стилей межкомнатных дверей</p>
								<p>В первую очередь – это классика: царица строгих линий, пропорциональности и легкости. Изделия, соответствующие всем ее требованиям должны быть выполнены из качественных материалов, особенно это касается самого полотна, для которого берут массив любой древесины. Традиционно классические двери - это глухое полотно, разделенное пополам филенками, но бывают и стеклянные, где полотно стекла закрепляется на декоративной решетке в верхней части полотна
								</p>
								<p class="title">Модерн или спокойный стиль дверей?</p>
								<p>
								Модерн спокойный и благородный стиль, уделяющий особое внимание плавным линиям и изогнутым формам. Двери, исполненные в его
								манере, можно узнать по декору - непрозрачные стекла с нежным растительными орнаментом, а также по наличию дополнительной подсветки
								на них, которая добавляет не только эстетичности, но и практичности.</p>

								<p>Смелый и роскошный стиль ар-деко своей напыщенной помпезностью может покорить любое сердце с первого взгляда, оставив незабываемые впечатления от увиденного. Двери, исполненные в соответствии с образом должны быть из драгоценных пород деревьев, только этим можно добиться эффектности. В качестве декора их разукрашивают прямолинейными растительными орнаментами, при этом особый акцент уделяют
								не только приглушенным, но и ярким цветам, леопардовые мотивы, золото, перекликающееся с матовым стеклом и т.д.</p>

								<p>Не менее роскошен и стиль барокко, украсивший двери золотом, яркими красками, с помощью которых появились неповторимые узоры,
								придающие изделию благородства и шика. Для тех, кому не хватает нежности, лучшим вариантом станут двери прованс,смотря на которые
								можно отправиться в мир мечты. Дверное полотно белого цвета – обычно это достигается искусственным старением, в качестве декора
