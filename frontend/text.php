                    <p id="green-country" class="title">1. Зелёная страна</p>
                    <p>Одноэтапная имплантация с немедленной нагрузкой представляет собой методику 
                    восстановления зубного ряда, при которой протезирование осуществляется сразу 
                    после установки имплантатов. 
                    Одноэтапная имплантация с немедленной нагрузкой применяется при:
                    частичной или полной адентии (отсутствии зубов) на всей челюсти;
                    атрофии костной ткани, когда есть возможность обойтись без процедуры синус-
                    лифтинга (наращивания кости);
                    необходимости быстрого восстановления жевательной и эстетической функции.
                    Важно понимать, что выбор метода имплантации зависит от конкретного 
                    клинического случая.</p>
                    <img src="images/articles/article-img-001.jpg">
                    <p id="security" class="title">2. Безопасность, как особенность</p>
                    <p>Одноэтапная имплантация с немедленной нагрузкой, как и любые другие 
                    стоматологические услуги, выполняются ведущей на территории Беларуси 
                    клиникой «Фабрика Улыбки». Клиника, основанная в 2003 году двумя талантливыми 
                    стоматологами Кассихиными – Александой и Еленой, за 14 летний стаж работы 
                    обрела репутацию одного из самых надежных медицинских стоматологических центров 
                    в Минске.
                    Высокая квалификацией всех, участвующих в лечении специалистов – Александра 
                    и Елены Кассихиных, Фоминой Елены, Николаенко Ивана и Молчана Александра 
                    подтверждена международными и национальными сертификатами.
                    Ряд врачей прошли обучение и стажировку у европейских (в университете 
                    имени Гете во Франкфурте у основоположника одноэтапной и базальной имплантации – 
                    доктора Стефана Иде) и израильских коллег (обучение в израильской стоматологической 
                    школе), имеют лицензию на работу на территории Европейского Союза. Средний стаж 
                    работы практикующего врача равен 20 годам. 
                    Персонал клиники говорит на английском языке.</p>
                    <p id="hospitality" class="title">3. Гостеприимство людей</p>
                    <img src="images/articles/article-img-002.jpg">
                    <p>Абсолютные противопоказания к установке имплантатов:
                    заболевания крови и кроветворных органов;
                    заболевания центральной нервной системы (врожденные и приобретенные);
                    злокачественные опухоли органов и систем у пациента;
                    системные заболевания соединительной ткани (ревматические, ревматоидные 
                    процессы, дерматозы, склеродермия и т.д.);
                    туберкулез и его последствия;
                    заболевания слизистой оболочки полости рта (хронический рецидивирующий - 
                    афтозный стоматит, красная волчанка, пузырчатка, синдром Шегрена, 
                    синдром Бехчета и пр.);
                    сахарный диабет.</p>
                    <p id="Availability" class="title">4. Доступность, как признак качества</p>
                    Обследование и санация полости рта пациента;
                    Удаление зубов (если требуется);
                    Установка в челюстную кость имплантата;
                    Накладывание швов вокруг имплантата для его фиксации в костной ткани;
                    Установка временных коронок на имплантат;
                    Установка постоянных коронок на имплантат.
                    Все хирургические манипуляции проводятся под местной анестезией, пациент 
                    не чувствует никаких болезненных ощущений.
                    <p id="historical-wealth" class="title">5. Историческое богатство</p>
                    <img src="images/articles/article-img-003.jpg">
                    <p>Оплата лечения производится в клинике после предоставления медицинских услуг. 
                    Ни клиникой, ни Beltreatment в стоимость лечения не включаются дополнительные 
                    агентские сборы с пациентов.</p>
                    <p id="science" class="title">6. Наука и инновации</p>
                    <p>Все индивидуальные рекомендации пациент получит после 
                    проведенного лечения у врача.
                    Общие рекомендации после проведения имплантации:
                    нежная чистка зубов мягкими щетками, не задевая швов;
                    употребление мягкой пищи — жидкой или протертой (диета соблюдается 
                    в период лечения и в течение двух недель после фиксации постоянного протеза);</p>
                    <p id="education" class="title">7. Образование и наследие</p>
